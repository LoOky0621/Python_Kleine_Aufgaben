# Hier sind einige Beispiele:

print(123 % 7)      # 4 -> 123 dividiert durch 7 ergibt 17 mit einem Rest von 4
print(12345 % 100)  # 45 -> 12345 dividiert durch 100 ergibt 123 mit einem Rest von 45
print(121 % 11)     # 0 -> 121 ist ein Vielfaches von 11, daher ist der Rest 0
print(9 % 2)        # 1 -> 9 dividiert durch 2 ergibt 4 mit einem Rest von 1
print(8 % 2)        # 0 -> 8 ist ein Vielfaches von 2, daher ist der Rest 0
print(3 % 5)        # 3 -> 3 dividiert durch 5 ergibt 0 mit einem Rest von 3

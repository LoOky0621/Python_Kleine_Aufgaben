# Aufgabenstellung: Vorhersage eines Ergebnisses
# Betrachte den folgenden Code, der eine Schleife verwendet, um eine bestimmte Abfolge von Operationen auszuführen.
# Vorhersage des Ergebnisses, das ausgegeben wird, wenn der Code ausgeführt wird. Analysiere den Code und erkläre, warum das Ergebnis so ist.

# Initialisierung der Variablen
z = -2
i = 99

# Schleife läuft, solange i größer als 88 ist
while i > 88:
    # z um 3 erhöhen
    z = z + 3
    # z negieren
    z = -z
    # i um 2 reduzieren
    i = i - 2

# Ausgabe des aktuellen Werts von z
print(z)

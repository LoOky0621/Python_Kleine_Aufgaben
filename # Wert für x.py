# Aufgabenstellung: Wert für x verändern
# Gegeben ist die Variable x mit dem Wert 12.
# Führe verschiedene Operationen mit x durch und gib den Wert von x nach jeder Operation aus.

# Initialisierung von x mit dem Wert 12
x = 12

# Ausgabe des aktuellen Werts von x
print("Aktueller Wert von x:", x)  # Erwartete Ausgabe: 12

# Addiere 12 zu x und aktualisiere den Wert von x
x += 12

# Ausgabe des aktualisierten Werts von x
print("Aktueller Wert von x nach Addition:", x)  # Erwartete Ausgabe: 24

# Subtrahiere 12 von x und aktualisiere den Wert von x
x -= 12

# Ausgabe des aktualisierten Werts von x
print("Aktueller Wert von x nach Subtraktion:", x)  # Erwartete Ausgabe: 12

# Teile x durch 12 und aktualisiere den Wert von x
x /= 12

# Ausgabe des aktualisierten Werts von x
print("Aktueller Wert von x nach Division:", x)  # Erwartete Ausgabe: 1.0

# Berechne den Modulo von x durch 12 und aktualisiere den Wert von x
x %= 12

# Ausgabe des aktualisierten Werts von x
print("Aktueller Wert von x nach Modulo-Berechnung:", x)  # Erwartete Ausgabe: 1.0
